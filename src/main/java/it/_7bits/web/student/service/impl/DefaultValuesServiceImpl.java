package it._7bits.web.student.service.impl;

import it._7bits.web.student.service.IDefaultValuesService;
import org.springframework.stereotype.Service;

/**
 * Implementation of service providing default values
 */
@Service
public class DefaultValuesServiceImpl implements IDefaultValuesService {
    private String departmentReturnUri;
    private String groupReturnUri;
    private String subDepartmentReturnUri;
    private String studentReturnUri;
    private String baseUrl;

    public String getDepartmentReturnUri() {
        return departmentReturnUri;
    }

    public void setDepartmentReturnUri(String departmentReturnUri) {
        this.departmentReturnUri = departmentReturnUri;
    }

    public String getGroupReturnUri() {
        return groupReturnUri;
    }

    public void setGroupReturnUri(String groupReturnUri) {
        this.groupReturnUri = groupReturnUri;
    }

    public String getSubDepartmentReturnUri() {
        return subDepartmentReturnUri;
    }

    public void setSubDepartmentReturnUri(String subDepartmentReturnUri) {
        this.subDepartmentReturnUri = subDepartmentReturnUri;
    }

    public String getStudentReturnUri() {
        return studentReturnUri;
    }

    public void setStudentReturnUri(String studentReturnUri) {
        this.studentReturnUri = studentReturnUri;
    }

    public String getBaseUrl() {
        return baseUrl;
    }

    public void setBaseUrl(String baseUrl) {
        this.baseUrl = baseUrl;
    }
}
